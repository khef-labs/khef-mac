export async function handleTool(
  name: string,
  args: Record<string, unknown>,
  client: KhefClient,
  _dbClient: DbClient
): Promise<ToolResult | null> {
  switch (name) {
    case "list_kapi_collections": {
      const result = (await client.listKapiCollections()) as {
        collections: Array<{
          id: string;
          handle: string;
          name: string;
          description: string | null;
          updated_at: string;
        }>;
      };
      if (result.collections.length === 0) {
        return {
          content: [
            {
              type: "text",
              text: "No kapi collections yet. Create one with create_kapi_collection.",
            },
          ],
        };
      }
      const text = result.collections.map(formatCollectionSummary).join("\n\n");
      return { content: [{ type: "text", text }] };
    }

    case "create_kapi_collection": {
      const result = await client.createKapiCollection({
        handle: args.handle as string,
        name: args.name as string,
        description: args.description as string | undefined,
      });
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "update_kapi_collection": {
      const { id, ...rest } = args as { id: string; [k: string]: unknown };
      const result = await client.updateKapiCollection(id, rest);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "delete_kapi_collection": {
      const result = await client.deleteKapiCollection(args.id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "list_kapi_definitions": {
      const result = (await client.listKapiDefinitions(
        args.collection_id as string
      )) as {
        definitions: Array<{
          id: string;
          handle: string;
          name: string;
          base_url: string | null;
          updated_at: string;
        }>;
      };
      if (result.definitions.length === 0) {
        return { content: [{ type: "text", text: "No kapi definitions in this collection." }] };
      }
      const text = result.definitions.map(formatDefinitionSummary).join("\n\n");
      return { content: [{ type: "text", text }] };
    }

    case "create_kapi_definition": {
      const result = await client.createKapiDefinition(args.collection_id as string, {
        handle: args.handle as string,
        name: args.name as string,
        description: args.description as string | undefined,
        base_url: args.base_url as string | undefined,
        default_auth: args.default_auth as Record<string, unknown> | undefined,
      });
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "update_kapi_definition": {
      const { id, ...rest } = args as { id: string; [k: string]: unknown };
      const result = await client.updateKapiDefinition(id, rest);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "delete_kapi_definition": {
      const result = await client.deleteKapiDefinition(args.id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "list_kapi_requests": {
      const result = (await client.listKapiRequests(args.definition_id as string)) as {
        requests: Array<{ id: string; name: string; method: string; path: string }>;
      };
      if (result.requests.length === 0) {
        return { content: [{ type: "text", text: "No requests in this definition." }] };
      }
      const text = result.requests
        .map((r) => `${r.method.padEnd(6)} ${r.name}\n  path: ${r.path}\n  id: ${r.id}`)
        .join("\n\n");
      return { content: [{ type: "text", text }] };
    }

    case "add_kapi_request": {
      const { definition_id, ...rest } = args as { definition_id: string; [k: string]: unknown };
      const result = await client.addKapiRequest(definition_id, rest as never);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "update_kapi_request": {
      const { id, ...rest } = args as { id: string; [k: string]: unknown };
      const result = await client.updateKapiRequest(id, rest);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "delete_kapi_request": {
      const result = await client.deleteKapiRequest(args.id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "run_kapi_request": {
      const result = (await client.runKapiRequest(args.request_id as string, {
        allow_insecure_tls: args.allow_insecure_tls as boolean | undefined,
        timeout_ms: args.timeout_ms as number | undefined,
      })) as { run: Parameters<typeof formatRun>[0] };
      return { content: [{ type: "text", text: formatRun(result.run) }] };
    }

    case "run_kapi_ad_hoc": {
      const { collection_id, ...rest } = args as { collection_id: string; [k: string]: unknown };
      const result = (await client.runKapiAdHoc(collection_id, rest as never)) as {
        run: Parameters<typeof formatRun>[0];
      };
      return { content: [{ type: "text", text: formatRun(result.run) }] };
    }

    case "list_kapi_runs": {
      const result = (await client.listKapiRuns(args.collection_id as string, {
        request_id: args.request_id as string | undefined,
        limit: args.limit as number | undefined,
      })) as {
        runs: Array<{
          resolved_method: string;
          resolved_url: string;
          response_status: number | null;
          response_time_ms: number | null;
          error: string | null;
          executed_at: string;
          id: string;
        }>;
      };
      if (result.runs.length === 0) {
        return { content: [{ type: "text", text: "No runs recorded." }] };
      }
      const text = result.runs
        .map((r) => {
          const status = r.error ? `ERR (${r.error})` : `${r.response_status}`;
          return `${r.executed_at}  ${r.resolved_method.padEnd(6)} ${r.resolved_url}\n  ${status}, ${r.response_time_ms} ms  (run ${r.id})`;
        })
        .join("\n\n");
      return { content: [{ type: "text", text }] };
    }

    case "get_kapi_run": {
      const result = await client.getKapiRun(args.id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "list_kapi_environments": {
      const result = await client.listKapiEnvironments(args.collection_id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "create_kapi_environment": {
      const { collection_id, ...rest } = args as { collection_id: string; [k: string]: unknown };
      const result = await client.createKapiEnvironment(collection_id, {
        handle: rest.handle as string,
        name: rest.name as string,
        is_active: rest.is_active as boolean | undefined,
      });
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "activate_kapi_environment": {
      const result = await client.activateKapiEnvironment(args.id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "set_kapi_env_var": {
      const result = await client.setKapiEnvVar(args.environment_id as string, {
        key: args.key as string,
        value: args.value as string,
        is_secret: args.is_secret as boolean | undefined,
        description: args.description as string | undefined,
      });
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "list_kapi_env_vars": {
      const result = await client.listKapiEnvVars(args.environment_id as string);
      return { content: [{ type: "text", text: fmt(result) }] };
    }

    case "copy_kapi_script": {
      const sourceId = args.source_request_id as string;
      const targetId = args.target_request_id as string;
      const kind = args.kind as "pre" | "test";
      const targetKind = (args.target_kind as "pre" | "test" | undefined) ?? kind;

      const source = (await client.getKapiRequest(sourceId)) as {
        request: {
          pre_script_content: string;
          pre_script_language: string;
          test_script_content: string;
          test_script_language: string;
        };
      };
      const contentField =
        kind === "pre" ? "pre_script_content" : "test_script_content";
      const languageField =
        kind === "pre" ? "pre_script_language" : "test_script_language";
      const targetContentField =
        targetKind === "pre" ? "pre_script_content" : "test_script_content";
      const targetLanguageField =
        targetKind === "pre" ? "pre_script_language" : "test_script_language";

      const content = source.request[contentField] ?? "";
      const language = source.request[languageField] ?? "javascript";

      await client.updateKapiRequest(targetId, {
        [targetContentField]: content,
        [targetLanguageField]: language,
      });

      return {
        content: [
          {
            type: "text",
            text: `Copied ${kind} script from ${sourceId} to ${targetId}${
              targetKind !== kind ? ` (as ${targetKind})` : ""
            }:\n\n${content || "(empty)"}`,
          },
        ],
      };
    }

    case "generate_kapi_jwt_script": {
      const alg = (args.alg as string | undefined) ?? "ES256";
      const tokenVar = (args.token_var as string | undefined) ?? "authToken";
      const expSeconds = (args.exp_seconds as number | undefined) ?? 300;
      const claims = (args.claims as Record<string, string> | undefined) ?? {};

      const content = jwtScriptTemplate({
        alg,
        keyVar: args.key_var as string,
        claimsVars: claims,
        tokenVar,
        expSeconds,
      });

      await client.updateKapiRequest(args.request_id as string, {
        pre_script_content: content,
        pre_script_language: "javascript",
      });

      return {
        content: [
          {
            type: "text",
            text: `Wrote ${alg} JWT pre-request script to request ${args.request_id}:\n\n${content}`,
          },
        ],
      };
    }

    case "generate_kapi_client_credentials_script": {
      const tokenPath = (args.token_path as string | undefined) ?? "/v1/oauth/tokens";
      const clientIdVar = (args.client_id_var as string | undefined) ?? "client_id";
      const clientSecretVar =
        (args.client_secret_var as string | undefined) ?? "client_secret";
      const tokenVar = (args.token_var as string | undefined) ?? "token";
      const tokenExpiryVar =
        (args.token_expiry_var as string | undefined) ?? "token_expiry";
      const refreshBufferSeconds =
        (args.refresh_buffer_seconds as number | undefined) ?? 60;
      const formFields =
        (args.form_fields as Record<string, string> | undefined) ?? {};

      const content = clientCredentialsScriptTemplate({
        tokenUrlVar: args.token_url_var as string,
        tokenPath,
        clientIdVar,
        clientSecretVar,
        audienceVar: (args.audience_var as string | undefined) ?? null,
        scopeVar: (args.scope_var as string | undefined) ?? null,
        tokenVar,
        tokenExpiryVar,
        refreshBufferSeconds,
        formFields,
      });

      await client.updateKapiRequest(args.request_id as string, {
        pre_script_content: content,
        pre_script_language: "javascript",
      });

      return {
        content: [
          {
            type: "text",
            text: `Wrote client_credentials pre-request script to request ${args.request_id}:\n\n${content}`,
          },
        ],
      };
    }

    default:
      return null;
  }
}
