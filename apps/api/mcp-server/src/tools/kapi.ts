function fmt(data: unknown): string {
  return JSON.stringify(data, null, 2);
}

function formatCollectionSummary(col: {
  id: string;
  handle: string;
  name: string;
  description: string | null;
  updated_at: string;
}): string {
  const lines = [`${col.name} (${col.handle})`, `  id: ${col.id}`];
  if (col.description) lines.push(`  ${col.description}`);
  lines.push(`  updated: ${col.updated_at}`);
  return lines.join("\n");
}

function formatDefinitionSummary(def: {
  id: string;
  handle: string;
  name: string;
  base_url: string | null;
  updated_at: string;
}): string {
  return `${def.name} (${def.handle}) — ${def.base_url ?? "no base url"}\n  id: ${def.id}\n  updated: ${def.updated_at}`;
}

function formatRun(run: {
  id: string;
  resolved_method: string;
  resolved_url: string;
  response_status: number | null;
  response_time_ms: number | null;
  error: string | null;
  response_body?: string | null;
  executed_at: string;
}): string {
  const lines: string[] = [];
  lines.push(`${run.resolved_method} ${run.resolved_url}`);
  if (run.error) {
    lines.push(`  ERROR: ${run.error}`);
  } else {
    lines.push(`  status: ${run.response_status} (${run.response_time_ms} ms)`);
  }
  lines.push(`  run_id: ${run.id}`);
  lines.push(`  at: ${run.executed_at}`);
  if (run.response_body) {
    const preview = run.response_body.length > 500
      ? run.response_body.slice(0, 500) + `\n... [${run.response_body.length - 500} more chars]`
      : run.response_body;
    lines.push(`  body:\n${preview.split("\n").map((l) => `    ${l}`).join("\n")}`);
  }
  return lines.join("\n");
}

/**
 * JWT-signer pre-request script template.
 * The sandbox host runtime (see sandbox.ts) exposes
 * khef.jwt.sign / khef.env / khef.request / khef.uuid / khef.log.
 */
function jwtScriptTemplate(input: {
  alg: string;
  keyVar: string;
  claimsVars: Record<string, string>;
  tokenVar: string;
  expSeconds: number;
}): string {
  const claimsLines = Object.entries(input.claimsVars)
    .map(([k, v]) => `  ${k}: ${v.startsWith("\"") ? v : `env.${v}`},`)
    .join("\n");
  // Collect every env var the template reads from: each claim whose value is
  // an env key (unquoted) + the signing key var. Dedupe to avoid redundant
  // lookups. Emit a FLAT JSON array so env.getAll sees [k1, k2, k3] — the
  // previous version wrapped JSON.stringify(array) inside another JS array
  // literal, producing a nested [[...], "keyVar"] shape that made
  // env.<keyVar> undefined.
  const envKeys = Array.from(
    new Set(
      [
        ...Object.values(input.claimsVars).filter((v) => !v.startsWith("\"")),
        input.keyVar,
      ],
    ),
  );
  return `// Auto-generated by generate_kapi_jwt_script
const env = khef.env.getAll(${JSON.stringify(envKeys)});
const now = Math.floor(Date.now() / 1000);
const claims = {
${claimsLines}
  iat: now,
  exp: now + ${input.expSeconds},
  jti: khef.uuid(),
};
const token = await khef.jwt.sign(claims, env.${input.keyVar}, { alg: "${input.alg}" });
khef.env.set("${input.tokenVar}", token);
khef.request.headers.set("Authorization", \`Bearer \${token}\`);
khef.log("Signed ${input.alg} JWT, exp ${input.expSeconds}s");
`;
}

/**
 * OAuth 2.0 client_credentials pre-request script template.
 * Emits a script that:
 *   - reads client_id/client_secret (and optional audience/scope) from env
 *   - checks a cached token + expiry, refreshes if within the buffer window
 *   - POSTs application/x-www-form-urlencoded to the token endpoint
 *   - writes the fresh token + expiry timestamp back to env
 *   - sets Authorization: Bearer on the outgoing request
 */
function clientCredentialsScriptTemplate(input: {
  tokenUrlVar: string;
  tokenPath: string;
  clientIdVar: string;
  clientSecretVar: string;
  audienceVar: string | null;
  scopeVar: string | null;
  tokenVar: string;
  tokenExpiryVar: string;
  refreshBufferSeconds: number;
  formFields: Record<string, string>;
}): string {
  const envKeys = Array.from(
    new Set(
      [
        input.tokenUrlVar,
        input.clientIdVar,
        input.clientSecretVar,
        ...(input.audienceVar ? [input.audienceVar] : []),
        ...(input.scopeVar ? [input.scopeVar] : []),
        ...Object.values(input.formFields).filter((v) => !v.startsWith("\"")),
      ],
    ),
  );

  const extraFormLines = Object.entries(input.formFields)
    .map(([k, v]) => `    ${JSON.stringify(k)}: ${v.startsWith("\"") ? v : `env.${v}`},`)
    .join("\n");

  const audienceLine = input.audienceVar
    ? `    audience: env.${input.audienceVar},\n`
    : "";
  const scopeLine = input.scopeVar ? `    scope: env.${input.scopeVar},\n` : "";

  return `// Auto-generated by generate_kapi_client_credentials_script
const env = khef.env.getAll(${JSON.stringify(envKeys)});
const tokenUrl = env.${input.tokenUrlVar} + ${JSON.stringify(input.tokenPath)};

const cachedToken = khef.env.get(${JSON.stringify(input.tokenVar)});
const expiryStr = khef.env.get(${JSON.stringify(input.tokenExpiryVar)}) || "0";
const expiryMs = parseInt(expiryStr, 10) || 0;
const bufferMs = ${input.refreshBufferSeconds} * 1000;

let token = cachedToken;
if (!token || Date.now() + bufferMs > expiryMs) {
  const body = new URLSearchParams({
    grant_type: "client_credentials",
    client_id: env.${input.clientIdVar},
    client_secret: env.${input.clientSecretVar},
${audienceLine}${scopeLine}${extraFormLines ? extraFormLines + "\n" : ""}  }).toString();

  const resp = await fetch(tokenUrl, {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body,
  });
  if (!resp.ok) {
    const errText = await resp.text();
    throw new Error(\`Token fetch failed: \${resp.status} \${errText}\`);
  }
  const json = await resp.json();
  token = json.access_token;
  if (!token) {
    throw new Error("Token response missing access_token: " + JSON.stringify(json));
  }
  const expiresMs = (json.expires_in ?? 3600) * 1000;
  khef.env.set(${JSON.stringify(input.tokenVar)}, token);
  khef.env.set(${JSON.stringify(input.tokenExpiryVar)}, String(Date.now() + expiresMs));
  khef.log("Fetched new access token, expires in " + Math.round(expiresMs / 1000) + "s");
} else {
  khef.log("Using cached access token");
}

khef.request.headers.set("Authorization", \`Bearer \${token}\`);
`;
}

